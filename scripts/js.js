function pictureHidden()
{
    var picHidden=document.getElementsByClassName("pictureHidden")
    if(picHidden[0].style.display=="block")
    {
    picHidden[0].style.display="none";
    }
    else
    {
    picHidden[0].style.display="block";
    }
}
